 let a = 3, b = 2;
 let z = a + b;

 console.log("resultado de la suma: " + z);

 z = a - b;
 console.log("resultado de la resta: " + z);

 z = a * b;
 console.log("resultado de la multiplicacion: " + z);

 z = a / b;
 console.log("resultado de la divicion: " + z);

 z = a % b;
 console.log("resultado de la operacion modulo o residuo: " + z);

 z = a ** b;
 console.log("resultado de la potencia, operador exponente: " + z);

// Operacion de incremento y decremento
// Pre-Incremento El operador ++  antes de la variable

z = ++a;
console.log(a);
console.log(z);

// Post-incremento el operador ++ despues de la varibale
z = b++;
console.log(b);
console.log(z);

//Pre-decremento 
z = --a;
console.log(a);
console.log(b);

//Post-decremento
z = b--;
console.log(b);
console.log(z);

